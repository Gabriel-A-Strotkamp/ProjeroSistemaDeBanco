    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
       if(agencia == null){
            agencia = new Agencias();
        }
       
        Funcionarios funcionarioSel = lstFuncionarios.getSelectedValue();
        if (funcionarioSel != null) {
            agencia.addFuncionarios(funcionarioSel);
            carregarFuncionariosCadastrados();

        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Funcionario para Adicionar");
        }

        carregarFuncionariosCadastrados();
    }//GEN-LAST:event_btnAddActionPerformed


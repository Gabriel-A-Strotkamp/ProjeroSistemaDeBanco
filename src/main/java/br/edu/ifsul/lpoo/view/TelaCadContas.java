    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        if(conta == null){
            conta = new Contas();
        }
        conta.setNumero(txtNConta.getText());
        conta.setTipoConta((Tipo_Contas)cmbTipoConta.getSelectedItem());
        conta.setSenha(txtSenha.getText());
        
       conta.setProprietario((Clientes) cmbProprietario.getSelectedItem());
        
       
        try {
            String texto = txtCredito.getText().replace(",", "."); // permite entrada com vírgula
            float valor = Float.parseFloat(texto);

            // Arredondar para duas casas decimais
            valor = (float) (Math.round(valor * 100.0) / 100.0);

            conta.setSaldoCredito(valor);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um valor numérico válido para o crédito.");
        }
        try {
            String texto = txtDebito.getText().replace(",", "."); // permite entrada com vírgula
            float valor = Float.parseFloat(texto);

            // Arredondar para duas casas decimais
            valor = (float) (Math.round(valor * 100.0) / 100.0);

            conta.setSaldoDebito(valor);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um valor numérico válido para o Debito.");
        }

        jpa.conexaoAberta();
        try {
            jpa.persist(conta);
            System.out.println("Conta cadastrada com sucesso");
        } catch (Exception ex) {
            Logger.getLogger(VerContas.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Erro ao persistir conta: "+conta+" \n Erro: "+ex);
        }
        jpa.fecharConexao();
        
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed


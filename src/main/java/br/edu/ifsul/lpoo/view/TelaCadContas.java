    public void carregarProprietario() {
    cmbProprietario.removeAllItems();
    cmbProprietario.addItem(null); // para permitir nenhum selecionado

    DefaultComboBoxModel<Clientes> modelo = new DefaultComboBoxModel<>();
    modelo.addElement(null); // para a opção vazia

    for (Clientes cliente : jpa.getClientes()) {
    modelo.addElement(cliente);
    }

        cmbProprietario.setModel(modelo);
    }
    
